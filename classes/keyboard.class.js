/**
 * Represents the state of the keyboard inputs used in the game.
 * Each property is a boolean indicating whether the corresponding key is currently pressed.
 */
class Keyboard {
    /**
     * True if the left arrow key is pressed.
     * @type {boolean}
     */
    LEFT = false;

    /**
     * True if the right arrow key is pressed.
     * @type {boolean}
     */
    RIGHT = false;

    /**
     * True if the up arrow key is pressed.
     * @type {boolean}
     */
    UP = false;

    /**
     * True if the down arrow key is pressed.
     * @type {boolean}
     */
    DOWN = false;

    /**
     * True if the spacebar is pressed.
     * @type {boolean}
     */
    SPACE = false;

    /**
     * True if the 'D' key is pressed.
     * @type {boolean}
     */
    D = false;
}